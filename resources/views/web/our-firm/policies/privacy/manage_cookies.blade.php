        <h2>Manage Cookies</h2>
        <p style="padding-top: 10px;">
        <div class="teaser-text">
            <p>Based on your browser version, browser settings, and device settings, the below instructions
                may vary for each user.</p>
            <p>Note: Changes made to your cookie settings as described below will change your settings for
                all websites accessed from the appropriate browser (unless you update them again at a later
                date). While disabling cookies will not impact your ability to access the&nbsp; content on
                this website, all sites operate differently. You may experience reduced functionality on
                other websites you visit.</p>
            <p></p>
            <h3>Please follow these instructions to <em>delete </em>your cookies based on your browser</h3>
            <h4 class="browser-title">Chrome</h4>
            <p>Go to settings (3 dots in the upper right of the browser window)</p>
            <p>Select “More Tools”</p>
            <p>Select “Clear Browsing Data”</p>
            <p>Select “Cookies and other site data”</p>
            <p>Click the “Clear Data” button<br>&nbsp;</p>
            <h4 class="browser-title">Mobile Chrome</h4>
            <p>Tap the menu button at the lower right of the screen (3 stacked dots)</p>
            <p>Choose “Settings”</p>
            <p>Under Privacy, tap “Clear Browsing Data”</p>
            <p>On the next screen, select “Cookies, Site Data” to uncheck this selection and tap the “Clear
                Browsing Data” link</p>
            <p>Select “Done”&nbsp;</p>
            <p>&nbsp;</p>
            <h4 class="browser-title">Firefox</h4>
            <p>Select the stacked line menu in the upper right (3 lines)</p>
            <p>Select “Options”</p>
            <p>Navigate to “Privacy and Security” in the left-side navigation</p>
            <p>Under the “History” heading, click the link “Clear History”</p>
            <p>Under “Time range to clear,” select “Everything”</p>
            <p>Using the “Details” drop down, make sure “Cookies” is checked and click the “Clear Now”
                button<br>&nbsp;</p>
            <h4 class="browser-title">Microsoft Edge</h4>
            <p>Open the&nbsp;menu (3 dots in upper right)</p>
            <p>Select “Settings”</p>
            <p>Under “Clear browsing data,” select “Choose What to Clear”</p>
            <p>Select “Cookies and saved website data” and click the “Clear” button<br>&nbsp;</p>
            <h4 class="browser-title">Internet Explorer</h4>
            <p>Open the settings menu (gear icon in upper right)</p>
            <p>Under “Internet Options,” navigate to the “General” tab and scroll down to the “Browsing
                History”</p>
            <p>Select "Cookies and website data"</p>
            <p>Click the "Delete" button<br>&nbsp;</p>
            <p>Note: Make sure that the “preserve websites favorites data” box is unchecked<br>&nbsp;</p>
            <h4 class="browser-title">Safari</h4>
            <p>Select the Safari &gt; Preferences menu item</p>
            <p>Select the “Privacy” tab</p>
            <p>Click “Manage Website Data” and click the “Remove All” button<br>&nbsp;</p>
            <h4 class="browser-title">Mobile Safari</h4>
            <p>Open the “Settings” app on your iPhone</p>
            <p>Scroll down and tap “Safari”</p>
            <p>Scroll down and tap “Clear History and Website Data”</p>
            <p>Confirm and tap the “Clear browsing data” button<br>&nbsp;</p>
            <h3>Please follow these instructions to <em>disable </em>your cookies based on your browser</h3>
            <h4 class="browser-title">Chrome</h4>
            <p>Click the 3 dots in the upper right corner</p>
            <p>Click “Settings"</p>
            <p>Using the left hand navigation, click “Advanced” to reveal a dropdown menu</p>
            <p>Click “Privacy and Security” and scroll down to “Content Settings”</p>
            <p>Click “Content Settings”</p>
            <p>Click “Cookies”</p>
            <p>Turn off “Allow sites to save and read cookie data”<br>&nbsp;</p>
            <h4 class="browser-title">Mobile Chrome</h4>
            <p>Tap the menu button at the lower right of the screen (3 stacked dots)</p>
            <p>Choose “Settings”</p>
            <p>Select “Site Settings”</p>
            <p>Turn off cookies and deselect “Allow third party cookies”<br>&nbsp;</p>
            <h4 class="browser-title">Firefox</h4>
            <p>Select the stacked line menu in the upper right (3 lines)</p>
            <p>Select “Options”</p>
            <p>Navigate to “Privacy and Security” in the left-side navigation</p>
            <p>Under the “History” heading, select “Use custom settings for history" under the dropdown menu
            </p>
            <p>Uncheck “Accept cookies from websites”<br>&nbsp;</p>
            <h4 class="browser-title">Microsoft Edge</h4>
            <p>Open the&nbsp;menu (3 dots in upper right)</p>
            <p>Select “Settings”</p>
            <p>Scroll down to “Advanced settings” and click “View Advanced Settings”</p>
            <p>Under the “Cookies” section, select “Block all cookies”<br>&nbsp;</p>
            <h4 class="browser-title">Internet Explorer</h4>
            <p>Open the settings menu (gear icon in upper right)</p>
            <p>Under “Internet Options” navigate to the “Privacy” tab and click “Advanced”</p>
            <p>Select “block” for each type of cookie you want to block (first party and third party)</p>
            <p>Click “OK” when finished<br>&nbsp;</p>
            <h4 class="browser-title">Safari</h4>
            <p>Select the Safari menu</p>
            <p>Select "Preferences"</p>
            <p>Click "Privacy"</p>
            <p>Under “Cookies and website data," select “Always block”<br>&nbsp;</p>
            <h4 class="browser-title">Mobile Safari</h4>
            <p>Open the “Settings” app on your iPhone</p>
            <p>Scroll down and tap “Safari”</p>
            <p>Under “Privacy and Security,” tap “Block Cookies” toggle to activate</p>
