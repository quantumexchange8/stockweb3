        <h2>@lang('public.fx disclosure') Statement</h2>
        <p style="padding-top: 10px;">
            Introduction
            <br><br>
            This FX Disclosure Statement (“Statement”) describes the relationship between clients and Wingate (“WAML”)1
            and/or its affiliates (collectively “WAML”) and discloses certain practices of WAML in pricing, handling and
            executing FX transactions in relation to its currency dealing business or currency execution services
            business. WAML may provide other products and/or services to clients, however unless explicitly referenced
            herein such products and/or services are not contemplated in this Statement.
            <br><br>
            This Statement supplements but does not supersede contracts governing the relationships between clients and
            WAML. In case of any conflict between this Statement and a contract, the contract shall take precedence.
            <br><br>
            Clients who have questions about this Statement or how WAML prices, handles or executes FX transactions may
            contact their FX Relationship Manager.
            <br><br>
            1. <span style="text-decoration: underline;">General practices</span><br>
            a. <span style="text-decoration: underline;">Communication methods</span>
            <br><br>
            Clients may communicate with WAML electronically, by phone or through other permissible channels.2 WAML may,
            at its discretion, agree to accept certain client instructions via email. Clients may be required to execute
            certain documents and/or meet specific WAML requirements prior to WAML accepting such instructions via
            email.
            <br><br>
            b. <span style="text-decoration: underline;">Information handling</span>
            <br><br>
            WAML has in place policies and procedures reasonably designed to protect the confidentiality of client and
            counterparty information by restricting the dissemination and use of confidential information. These
            policies and procedures generally restrict the dissemination of client confidential information to parties
            who ‘need to know’ in order to execute transactions or otherwise perform under the agreed upon arrangement,
            and manage subsequent events related to such execution or performance. They also provide that client
            confidential information may only be used for legitimate business purposes to provide a product, perform a
            service or conduct oversight of a product or service that WAML has been contracted, or is expected, to
            perform. However, WAML may use sufficiently aggregated and anonymized client confidential information to
            form views on market trends that it may share with clients (for example, market color). Additionally, as a
            regulated entity, WAML may be required to share client confidential information with relevant global
            regulators.
            <br><br>
            c. <span style="text-decoration: underline;">Conflicts of interest</span>
            <br><br>
            Although WAML takes all reasonable steps to prevent conflicts of interest from adversely affecting the
            interests of its clients, WAML’s own interest may at times conflict with the interests of its clients and
            its clients’ interests may conflict with one another. For example, WAML may execute transactions and carry
            positions in various FX products including spot, forwards (including non-deliverable forwards) and swaps on
            its book in connection with actual or anticipated client trade requests, hedging strategies or otherwise.
            WAML’s activities in connection with such transactions and positions could impact prices and available
            liquidity, potentially to the detriment of a client. When WAML accepts and attempts to fill a client order,
            it may be engaged in FX trading activities for its own benefit in the same or similar FX products. Where
            WAML provides other products and/or services to a client, the provision of such products and/or services may
            also lead to other conflicts of interest.
            <br><br>
            2. <span style="text-decoration: underline;">WAML transacts with clients in a principal capacity</span>
            <br><br>
            WAML executes FX transactions on a principal basis as counterparty to its clients. When acting in a
            principal capacity, WAML: (i) trades for its own account, at its own risk, for its benefit; and (ii) does
            not act as an agent, fiduciary, financial advisor or other similar capacity for or on behalf of clients.
            Clients who trade with WAML acting in a principal capacity are expected to independently evaluate the
            appropriateness of any proposed transaction. Furthermore, any statement(s) made by WAML trading personnel or
            distributed through a WAML electronic system must not be construed or relied upon as advice or a
            recommendation.
            <br><br>
            a. <span style="text-decoration: underline;">Pricing</span>
            <br><br>
            Any price quotes or streaming rates WAML provides are an ‘all in’ indicative price. WAML exercises
            discretion in tailoring its indicative pricing for individual clients / transactions taking into account a
            variety of commercial factors. These factors may include, but are not limited to: (i) prevailing market
            conditions; (ii) the size of the order; (iii) time of day; (iv) liquidity of the currency(ies); (v) WAML’s
            relationship with the client; (vi) the costs WAML may incur in connection with the transaction; and (vii)
            the credit, settlement, operational and other risks of the transaction. WAML may differentiate its
            indicative pricing by trading venue, platform or communication method. As such, WAML may provide different
            indicative prices for the same or similar transactions to different clients. WAML may change its pricing
            strategies without notice and is not obliged to disclose the components of its ‘all in’ price.
            <br><br>
            WAML may from time to time agree to execute orders using alternative pricing arrangements (see for example,
            Benchmark and fixing rates and Algorithms).
            <br><br>
            b. <span style="text-decoration: underline;">Pre-hedging and pre-positioning</span>
            <br><br>
            WAML may engage in pre-hedging and hedging activities based on its knowledge of its clients’ trading
            activities. WAML will endeavor to conduct such pre-hedging and hedging activities in a manner that does not
            disadvantage clients and with an intent to avoid materially impacting market prices. WAML conducts
            pre-hedging and hedging activities on a principal basis in respect of its principal activities, at its own
            risk, and not as an agent for clients.
            <br><br>
            c. <span style="text-decoration: underline;">Benchmark and fixing rates</span>
            <br><br>
            WAML may agree to execute a client order based on a benchmark or fixing rate. While WAML does not
            intentionally attempt to affect benchmark or fixing rates, it may engage in hedging or other FX activities
            that may impact ultimate benchmark or fixing rates.
            <br><br>
            d. <span style="text-decoration: underline;">Order handling</span>
            <br><br>
            When WAML acknowledges, accepts or states that it is willing to ‘work’ a client order, it will attempt but
            is not contractually bound to execute the trade at or near the requested price and quantity. WAML is not
            obligated to accept client orders and may accept or reject any order for any reason in its sole discretion.
            The fact that a condition to which an order is subject (for example, a stop loss order) has been triggered
            does not mean that WAML can or will complete the order at the requested price and/or quantity. WAML may
            sequence / prioritize client orders in its sole discretion and may aggregate client orders for the purpose
            of pricing.
            <br><br>
            e. <span style="text-decoration: underline;">Handling of requests received via electronic trading platforms (Last look)</span>
            <br><br>
            Any price quotes or streaming rates that WAML provides are an ‘all in’ indicative price. Clients may submit
            an electronic trade request (an offer) to trade with WAML in response to such ‘all in’ indicative prices
            and, if so, WAML may accept or reject that electronic trade request in its sole discretion.
            <br><br>
            f. <span style="text-decoration: underline;">Algorithmic trading</span>
            <br><br>
            WAML may employ algorithmic trading strategies in executing principal FX trades for its own risk management
            purposes. WAML may also provide clients access to its algorithmic execution service (“Algo Execution
            Service”). The Algo Execution Service allow a client to request to enter into an FX transaction with WAML,
            at a price based on rate(s) derived from offsetting trade(s) between WAML and liquidity provider(s) and
            subject to any agreed upon mark-ups/downs by WAML. Under the Algo Execution Service, the aforementioned
            offsetting trade(s) between WAML and liquidity provider(s) would be arrived at based on the client’s
            requested algorithm and other parameters in accordance with the applicable Algorithmic Execution Terms &
            Conditions.
            <br><br>
            3. <span style="text-decoration: underline;">Agency FX</span><br>
            a. <span style="text-decoration: underline;">Currency Execution Services</span>
            <br><br>
            Wingate Investor Services Limited (“WAMLISL”) may agree to act as an agent in executing client trades with
            client designated clearing brokers or approved bank counterparties as a part of its Currency Execution
            Services (“CES trades”). The pricing of CES trades will be based upon rates derived from a panel of
            liquidity providers plus, as applicable, a prearranged fee. Agency trades are conducted in accordance with
            the WAMLISL Order Handling Policy. WAMLISL may delegate responsibility for discharging certain functions
            related to CES trades to WAML, which also engages in a principal trading business. As such, there is a risk
            of a conflict of interest between WAML’s agency and principal trading businesses.
            <br><br>
            WAMLISL may in its discretion aggregate the CES trades of multiple clients for pricing purposes. While it is
            unlikely that the aggregation of orders and transactions will work overall to the disadvantage of any client
            whose orders are to be aggregated, the effect of aggregation may work to a client’s disadvantage in relation
            to a particular order.
            <br><br>
            b. <span style="text-decoration: underline;"></span>Other Agency activities
            <br><br>
            In certain other instances, WAML may execute FX transactions as an agent for its clients. In such instances,
            WAML acts as an agent solely for purposes of execution and does not otherwise act as an agent, fiduciary,
            financial advisor or other similar capacity for or on behalf of clients.
            <br><br>
            c. <span style="text-decoration: underline;"></span> Pre-hedging and pre-positioning
            <br><br>
            WAMLISL does not engage in pre-hedging or pre-positioning when acting in an agency capacity.
            <br><br>
            ¹ As of the date of publication of this Statement, WAML is the only WAML entity that executes FX
            transactions on a principal basis. <br>
            ² WAML may record phone communications as permitted under applicable local rules.
