        <h2>Investment Strategy & Transaction Types</h2>
        <p style="padding-top: 10px;">
            The flexibility of our investment mandate is a key differentiator for management teams and co-investors. We
            can invest in a range of securities, including equity, subordinated debt, or some combination of the two.
            Unlike other funds, there is no predetermined formula for the equity and debt split, which allows us to
            customize a capital structure suited to each investment. <br><br>

            Wingate ("WAML") invests in the following transaction types: <br><br>

            <strong>Control Transactions:</strong> <br><br>

            We take a majority stake in the company and serve as the financial sponsor: <br>
            &nbsp;&nbsp;&nbsp;&bull;&nbsp;&nbsp;The typical structure includes a one-stop junior capital solution
            composed of an equity and subordinated debt investment by WAML. <br>
            &nbsp;&nbsp;&nbsp;&bull;&nbsp;&nbsp;This is an attractive option to owner-operators focused on the certainty
            of close that comes with one junior capital provider. <br><br>

            In minority/non-control transactions and when partnering with operating executives or investors, our capital
            supports management buyouts, growth, recapitalizations, venture-backed companies whose investor base may
            have varying liquidity goals, or investors seeking capital for partial liquidity and/or growth. <br><br>

            <strong>Minority/Non-Control Transactions:</strong> <br>
            We are the lone institutional investor with a minority stake in an owner-operated business, otherwise a
            significant minority investor in a business with institutional investors seeking an additional financial
            partner: <br>
            &nbsp;&nbsp;&nbsp;&bull;&nbsp;&nbsp;We typically structure investments as straight preferred or common
            equity, or some combination of equity and subordinated debt.<br>
            &nbsp;&nbsp;&nbsp;&bull;&nbsp;&nbsp;Our investments are ideal for family-owned or privately held businesses
            in need of capital but seeking to retain economic control.<br>
            <strong>Partnering with Operating Executives or Other Investors:</strong><br>
            &nbsp;&nbsp;&nbsp;&bull;&nbsp;&nbsp;We provide a mix of equity and subordinated debt to a family investment
            office or fundless sponsor with an existing portfolio company or new investment opportunity that requires a
            primary capital provider.<br>
            &nbsp;&nbsp;&nbsp;&bull;&nbsp;&nbsp;We back executives with capital to complete a management or leveraged
            buyout of a business identified by the executive or us.<br>
