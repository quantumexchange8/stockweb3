        <p style="padding-top: 10px;">
            Most enduring businesses consider and plan for management succession. For family businesses, this topic can
            be particularly pertinent, especially where management is deciding between multiple family members or a
            nonfamily member as the heir apparent CEO. Management succession for family businesses tends to have an
            additional level of complexity because it must balance the traditional challenges of having a successful
            plan with the family dynamics inherent in a family enterprise. <br><br>

            Family businesses also face the challenge of ownership succession. In many cases, the next generation of
            owners may not be employed in the company, though they will be shareholders. This can lead to issues in the
            absence of a clear articulation of long-term objectives and a carefully considered set of share ownership
            and transfer policies. <br><br>

            WAML has worked with family-owned businesses for generations on management and ownership succession
            planning, and we regularly consider this topic ourselves as a multigenerational partner-owned private
            business. When helping our clients create solutions to their succession challenges, we work with them to
            consider a number of questions, including: <br><br>
            &nbsp;&nbsp;&nbsp;&bull;&nbsp;Upon transition, how does the present management and ownership model impact
            the success of the next generation of family managers and owners? Nonfamily managers?<br><br>
            &nbsp;&nbsp;&nbsp;&bull;&nbsp;What succession planning alternatives are being considered, if any?<br><br>
            &nbsp;&nbsp;&nbsp;&bull;&nbsp;What discussions around management and/or ownership of the business have been
            had with the next generation? How are family members being prepared for leadership roles?<br><br>
            &nbsp;&nbsp;&nbsp;&bull;&nbsp;How are leadership succession decisions made? Is the process well-understood
            by both family and nonfamily leaders?<br><br>
            &nbsp;&nbsp;&nbsp;&bull;&nbsp;Is there a process to educate/train rising generations of
            owners/leaders?<br><br>
            &nbsp;&nbsp;&nbsp;&bull;&nbsp;What guidelines have been established for stock ownership and ownership
            transfers?<br><br>
