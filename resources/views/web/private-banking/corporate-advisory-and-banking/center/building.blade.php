        <h2>Building a Support Team</h2>
        <p style="padding-top: 10px;">
            Family businesses benefit meaningfully from having a network of external advisors. In our experience, many
            families find that investing time to identify and nurture relationships with other families, contemporaries
            and professional advisors is of great benefit and value to the family and the business. <br><br>

            WAML works with family business owner clients to help solidify their advisory teams by considering important
            questions, including: <br><br>
            &nbsp;&nbsp;&nbsp;&bull;&nbsp;Who in the family or its professional network can owners confide in to discuss
            the business, their family, their family’s wealth and the intersection of these items?
            <br><br>
            &nbsp;&nbsp;&nbsp;&bull;&nbsp;How confident are owners that they have access to the expertise and skills
            necessary to be an effective owner and/or operator?<br><br>
            &nbsp;&nbsp;&nbsp;&bull;&nbsp;How can owners identify individuals who can provide a well-rounded perspective
            on them, the family and the business?<br><br>
            &nbsp;&nbsp;&nbsp;&bull;&nbsp;What are any advisors’ interests? Are fee arrangements
            well-understood?<br><br>
            &nbsp;&nbsp;&nbsp;&bull;&nbsp;Have any conflicts of interests been discussed? How will they be mitigated?
            <br><br>
            &nbsp;&nbsp;&nbsp;&bull;&nbsp;What service providers have owners engaged for assistance in navigating both
            the business and the family?<br><br>
